let number1 = parseFloat(prompt('Ingrese un primer numero:'));
let number2 = parseFloat(prompt('Ingrese un segundo numero:'));
let suma = number1 + number2;
let resta = number1 - number2;
let multiplo = number1 * number2;
let division = number1 / number2;

alert(`El resultado de su suma es ${suma}`);
alert(`El resultado de su resta es ${resta}`);
alert(`El resultado de su multiplicación es ${multiplo}`);
alert(`El resultado de su división es ${division}`);
